var daysUntilMyBirthday = 60;

while (daysUntilMyBirthday > 0) {
    if(daysUntilMyBirthday > 30) {
        console.log('Uuuuuuggghhhhhh. ' + daysUntilMyBirthday + ' days until my birthday. This world sucks.')
    } else if (daysUntilMyBirthday > 10) {
        console.log(daysUntilMyBirthday + ' is still way too much.')
    } else if (daysUntilMyBirthday == 1) {
        console.log('TOMORROW!')
    } else if (daysUntilMyBirthday < 4) {
        console.log(daysUntilMyBirthday + ' days left! EEEEEeeeee!')
    } else if (daysUntilMyBirthday <= 10) {
        console.log('Finally. Only ' + daysUntilMyBirthday + ' days till my B-Day Dawg! You better getch yo presents ready.')
    }

    daysUntilMyBirthday -= 1;
}

